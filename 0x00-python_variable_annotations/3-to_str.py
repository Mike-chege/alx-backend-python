#!/usr/bin/env python3
"""
This function to_str that takes a float n
As argument and returns the string representation
Of the float
"""


def to_str(n: float) -> str:
    """
    Convert a floating-point number to its string representation.

    Parameters:
    - n (float): The input floating-point number.

    Returns:
    str: The string representation of the input number.
    """
    result = str(n)
    return result
